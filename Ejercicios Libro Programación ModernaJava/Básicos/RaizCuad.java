package Básicos;
// Construir un programa que calcule la raíz cuadrada de 90
public class RaizCuad{
    public static void main(String args[]){
        double argum = 90, result = 0;
        result = Math.sqrt(argum);
        System.out.println("La raiz cuadrada de " + argum + " es " + result);
    }
}